    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        
        try{
        int index = jComboBox1.getSelectedIndex();
        String name = (String) jComboBox1.getSelectedItem();
        int elem = Integer.parseInt(jTextField1.getText());
        int delay = Integer.parseInt(jTextField2.getText());
            if(elem > 900){
              JOptionPane.showMessageDialog(null, "Cannot Go Beyond 900 elements!");
            }else if(elem < 5){
              JOptionPane.showMessageDialog(null, "Must have at least 5 elements!");
            }else if(delay < 0){
              JOptionPane.showMessageDialog(null, "Negative Delay not allowed!");
            }else{
                TimerClass timez = new TimerClass();
                timez.TimerStart();
                AnimationInitialize.begin(elem, delay, index, name);
                timez.done();
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Invalid Data Given!");
        }
    }
    
